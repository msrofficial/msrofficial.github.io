    scrollReveal.reveal('.about-text, .about-image', { delay: 200, interval: 200 });
    scrollReveal.reveal('.skill-card', { delay: 200, interval: 200 });
    scrollReveal.reveal('.project-card', { delay: 200, interval: 200 });
    scrollReveal.reveal('.contact-info, .contact-form', { delay: 200, interval: 200 });

